package domain;

public class EspecialidadVO {
	
	private String nombre;

	public EspecialidadVO() {
		super();
	}

	public EspecialidadVO(String nombre) {
		super();
		this.nombre = nombre;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	

}
